//    Xử lý hiển thị dữ liệu được click từ table lên form
    public void showDetail() {
        int index = tblHocVien.getSelectedRow();
        Student std = list.get(index);

        txtName.setText(list.get(index).getName());
        txtMark.setText(Double.toString(std.getMark()));
        txtGrade.setText(std.getRank());
        cboCourse.setSelectedItem(std.getCourse());
        chkGift.setSelected(std.isBouns());
    }

