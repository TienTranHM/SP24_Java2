//    Thêm mới đối tượng Student vào list
    public void addStudent() {
        Student student = new Student();
        student.setName(txtName.getText());
        try {
            student.setMark(Double.parseDouble(txtMark.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Marks must be number");
            txtMark.requestFocus();
        }
        student.setCourse((String) cboCourse.getSelectedItem());
        list.add(student);

        txtGrade.setText(student.getRank());
        chkGift.setSelected(student.isBouns());
    }
    
//    Hiển thị dữ liệu từ list ra table
    public void fillToTable() {
        String[] header = {"Name", "Mark", "Course", "Grade", "Gift"};
        DefaultTableModel model = new DefaultTableModel(header, 0);

        for (Student student : list) {
            Object[] row = new Object[]{student.getName(), student.getMark(), student.getCourse(), student.getRank(), student.isBouns() ? "Gift" : "No"};
            model.addRow(row);
        }

        tblHocVien.setModel(model);
    }
